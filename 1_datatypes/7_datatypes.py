# Tuples
# Tuples are immutable

masala_spices = ("cinnamon", "cardamom", "clove")

# Unpacking the tuple into three variables
spice1, spice2, spice3 = masala_spices

print(f"Spice 1: {spice1}")
print(f"Spice 2: {spice2}")
print(f"Spice 3: {spice3}")

# Ratios
ginger_ratio = 0.1
cardamom_ratio = 0.2
cinnamon_ratio = 0.3

print(f"Ginger ratio: {ginger_ratio}")
print(f"Cardamom ratio: {cardamom_ratio}")
print(f"Cinnamon ratio: {cinnamon_ratio}")

# Membership checks
print(f"Is cinnamon in masala_spices: {'cinnamon' in masala_spices}")
print(f"Is pepper in masala_spices: {'pepper' in masala_spices}")
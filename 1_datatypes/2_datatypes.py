# Numbers
# Integers: non-fractional part
# Floating point: with fractional part
# Complex numbers

# Strings: collection of characters used for textual data

# Integer values
black_tea_grams = 13
ginger_grams = 5

# Addition
total_grams = black_tea_grams + ginger_grams
print(f"Total grams: {total_grams}")

# Subtraction
remaining_tea = black_tea_grams - ginger_grams
print(f"Remaining tea: {remaining_tea}")

# Division
milk_litres = 7
servings = 4
milk_per_serving = milk_litres / servings
print(f"Milk per serving: {milk_per_serving}")

# Floor Division
total_tea_bags = 7
pots = 4
bags_per_pot = total_tea_bags // pots
print(f"Tea bags per pot: {bags_per_pot}")